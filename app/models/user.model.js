const mongoose = require('mongoose');
const userSchema = mongoose.Schema({
    username: {
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true
    },
    password:{
        type:String,
        required:true
    },
    created_at: {
        type: Date,
        default: Date.now 
    },
    updated_at: {
        type: Date,
        default: Date.now 
    }
})

let userModel = mongoose.model('users',userSchema);
module.exports = userModel;
